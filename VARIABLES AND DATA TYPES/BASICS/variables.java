package BASICS;

public class variables {
    public static void main(String[] args) {
        // Variable declaration
        int a = 10; // Integer variable
        double b = 20.5; // Double variable
        char c = 'A'; // Character variable
        String name = "Rushikesh"; // String variable
        byte d = 100; // Byte variable
        boolean isTrue = true; // Boolean variable
        long e = 100000L; // Long variable
        float f = 10.5f; // Float variable
        short g = 30000; // Short variable

        // Printing the variables declared above
        System.out.println("Integer: " + a);
        System.out.println("Double: " + b);
        System.out.println("Character: " + c);
        System.out.println("String: " + name);
        System.out.println("Byte: " + d);
        System.out.println("Boolean: " + isTrue);
        System.out.println("Long: " + e);
        System.out.println("Float: " + f);
        System.out.println("Short: " + g);
    }
}
